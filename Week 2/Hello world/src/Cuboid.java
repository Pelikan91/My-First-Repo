public class Cuboid {
    public static void main(String[] args) {

        double a, b, c;
        a = 6.30;
        b = 2.50;
        c = 3.20;

        System.out.println("Surface area of cuboid is " + a * b * c + ".");
        System.out.println("Volume of cuboid is " + 2 *(a*b + b*c + a*c) + ".");

    }
}